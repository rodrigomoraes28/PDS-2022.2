package Questao4;

public class Usuario {

  String nome;
  int idade;

  public Usuario(string nome_ex , int idade_ex){
      this.nome = nome_ex;
      this.idade = idade_ex;
  }

  public int getIdade() {
    return idade;
  }

  public String getNome() {
    return nome;
  }

  public void setIdade(int idade) {
    this.idade = idade;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }
  
}
